package CarBundle;

public class CarType {
	String brand;
	String model;
	String color;
	int speed;
	int distance;

	CarType(String brand, String model, String color, int speed, int distance) {
		this.brand = brand;
		this.model = model;
		this.color = color;
		this.speed = speed;
		this.distance = distance;
	}
}